function getSched(i) {
  const ct = JSON.parse(localStorage.getItem('ct_' + i) || '[]');
  const cr = JSON.parse(localStorage.getItem('cr_' + i) || '[]');
  const gym = isGym(i);
  const party = isCafe(i);

  let L = [];
  if (gym) {
    // برنامه باشگاه کاربر از localStorage
    const gymPlan = JSON.parse(localStorage.getItem('gym_plan_' + i) || '[]');
    gymPlan.forEach(ex => L.push({time: ex.time || '', e: '🏋️', t: ex.name, s: ex.sets || ''}));
  }
  if (party) {
    L.push({time: '', e: '🎉', t: 'خوشگذرونی امروز!', s: 'لذت ببر'});
  }

  const customItems = ct.map(x => {
    const parts = x.split('|');
    return {time: parts[2] || '', e: parts[3] || '📌', t: parts[0] || '', s: parts[1] || ''};
  });
  L = [...L, ...customItems];

  return {L, R: [], ct, cr};
}

