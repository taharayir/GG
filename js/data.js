function getPD(i) {
  const months = [
    {n:'خرداد', days:31, start:24},
    {n:'تیر', days:31, start:1},
    {n:'مرداد', days:31, start:1},
    {n:'شهریور', days:31, start:1},
    {n:'مهر', days:30, start:1},
    {n:'آبان', days:30, start:1},
    {n:'آذر', days:30, start:1},
    {n:'دی', days:30, start:1},
    {n:'بهمن', days:30, start:1},
    {n:'اسفند', days:29, start:1},
  ];
  let day = i;
  let remaining = months[0].days - months[0].start + 1;
  if (day < remaining) return {d: months[0].start + day, m: months[0].n};
  day -= remaining;
  for (let m = 1; m < months.length; m++) {
    if (day < months[m].days) return {d: months[m].start + day, m: months[m].n};
    day -= months[m].days;
  }
  return {d: day + 1, m: 'اسفند'};
}

